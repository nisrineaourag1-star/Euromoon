package be.euromoon.model.reis;

/**
 * Geeft de mogelijke rollen aan die personeelsleden
 * kunnen vervullen tijdens een reis.
 */
public enum ReisRol {

    /**
     * Personeelslid dat verantwoordelijk is voor het besturen
     * van de trein.
     */
    BESTUURDER,

    /**
     * Personeelslid dat instaat voor de begeleiding en service
     * van passagiers tijdens de reis.
     */
    STEWARD,

    /**
     * Personeelslid dat verantwoordelijk is voor het laden,
     * lossen en beheren van bagage.
     */
    BAGAGE
}
